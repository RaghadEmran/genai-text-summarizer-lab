from openai import OpenAI

# Create client using GitHub Models endpoint
client = OpenAI(
    base_url="https://models.github.ai/inference/v1",
    api_key=GITHUB_TOKEN
)

print("Client created successfully.")

question = "What are the benefits of using GitHub Marketplace Models?"

prompt = f"Answer the following question clearly and concisely:\n\n{question}"

response = client.chat.completions.create(
    model="gpt-4o-mini",
    messages=[
        {"role": "user", "content": prompt}
    ]
)

print("Q:", question)
print("A:", response.choices[0].message.content)

"""## Mini Project: Text Summarizer

This project summarizes any paragraph (Arabic or English) into exactly three concise bullet points.

"""

def summarize_text(text):
    """
    Summarizes a given paragraph (Arabic or English) into
    exactly three short bullet points.
    """
    system_prompt = (
        "You are a helpful assistant that takes any English or Arabic paragraph "
        "and summarizes it into exactly 3 concise bullet points. "
        "Each bullet must be one short sentence. "
        "Do NOT write anything else before or after the bullets."
    )

    response = client.chat.completions.create(
        model="gpt-4o-mini",
        messages=[
            {"role": "system", "content": system_prompt},
            {"role": "user", "content": text}
        ]
    )

    return response.choices[0].message.content

paragraph = input("Paste your paragraph to summarize:\n\n")

summary = summarize_text(paragraph)

print("\n--- Summary (3 bullet points) ---\n")
print(summary)

